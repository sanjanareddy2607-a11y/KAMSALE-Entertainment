export function filterVenues(query: string, limit = 8): string[] {
  const normalized = query.trim().toLowerCase();
  if (!normalized) return [...VENUE_LOCATIONS].slice(0, limit);

  return VENUE_LOCATIONS.filter((venue) =>
    venue.toLowerCase().includes(normalized)
  ).slice(0, limit);
}
